
get.pretty.parameter.names <- function(params)
{
    rv = PRETTY.PARAMETER.NAMES[as.character(params)]
    if (any(is.na(rv) | rv==''))
    {
        stop(paste0("Pretty names have not been set for the following parameters: ",
                    paste0(params[is.na(rv) | rv==''], collapse=', ')))
    }
    
    rv
}

